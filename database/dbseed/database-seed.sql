-- dataseries contains the time series configuration data.
create sequence seq_dataseries__id;
create table dataseries
(
    id          bigint unique            not null default nextval('seq_dataseries__id'),
    created_at  timestamp with time zone not null default current_timestamp,
    updated_at  timestamp with time zone not null default current_timestamp,
    name        varchar(255)             not null,
    description text                              default null,
    label       varchar(255)                      default null,
    api_key     varchar(255)             not null,
    primary key (id)
);

-- dataseries_summaries is a materialized view for getting the newest value in
-- a time series.
-- The application is expected to update current_value, min_value and max_value
-- whenever a new value is added to the readings table.
create sequence seq_dataseries_summaries__id;
create table dataseries_summaries
(
    id            bigint unique                                  not null default nextval('seq_dataseries_summaries__id'),
    created_at    timestamp with time zone                       not null default current_timestamp,
    updated_at    timestamp with time zone                       not null default current_timestamp,
    current_value varchar(255)                                   not null,
    min_value     varchar(255)                                   not null,
    max_value     varchar(255)                                   not null,
    dataseries_id bigint references dataseries (id)
        constraint uk_dataseries_summaries__dataseries_id unique not null,
    primary key (id)
);

-- readings will contain the values received from the devices. Each value is
-- associated with a dataseries but there is no foreign key constraint in order
-- to make inserts a little bit faster.
create sequence seq_readings__id;
create table readings
(
    id            bigint unique            not null default nextval('seq_readings__id'),
    created_at    timestamp with time zone not null default current_timestamp,
    updated_at    timestamp with time zone not null default current_timestamp,
    value         varchar(255)             not null,
    dataseries_id bigint                   not null,
    primary key (id)
);

-- The readings are most often queried given the dataseries id and the number
-- of newest readings.
CREATE INDEX idx_readings_dataseries__created_at ON readings (dataseries_id, created_at DESC);

